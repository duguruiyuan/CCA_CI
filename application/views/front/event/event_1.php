   <div style="width:800px; margin:0 auto;"> 
      <h2 style="text-align:center; margin-top:30px;"> 中国胸痛中心认证工作委员会第一次执行委员会会议召开</h2>
<p style="text-align:center; margin-top:10px;">2014年2月12日</p>
      <br>
     <p style="text-align:center;" > <img  src="../img/event_1_1.jpg"></p><br>
      <p style="text-indent:2em;line-height:30px; font-size:16px;">2014年2月12日，中国胸痛中心认证工作委员会第一次执行委员会会议在北京举行。根据去年11月11日中国胸痛中心认证工作委员会第一次全体会议的决议，中国胸痛中心认证工作办公室组织12位专家在2个月内完成了首批申请认证的四家医院的材料审查和现场核查工作。本次执行委员会会议的主要内容是对首次认证情况进行分析和总结、投票表决认证结果、讨论认证标准和流程的修订以及下一步的工作计划。</p>
<p style="text-indent:2em;line-height:30px; font-size:16px;">工作委员会主任委员霍勇教授和执行主任委员方唯一教授担任本次会议主席，会议由工作委员会副主任委员兼秘书长向定成教授主持。参与现场核查的11位专家霍勇、方唯一、向定成、陈纪言、王乐丰、陈韵岱、秦伟毅、刘健、曲新凯、伍贵富、李浪、心血管病学分会办公室宁田海主任、李冬梅主任、张岩主任及认证办公室易绍东主任和夏斌秘书出席了本次会议。会议重点讨论了以下问题：
</p>
    <h4 style="text-align:left; margin-top:30px;"> 一、四家申请认证单位的评审情况总结：</h4>  
   <p  style="text-indent:2em;line-height:30px; font-size:16px;">首先，认证办公室主任易绍东博士就以下内容做了详细汇报：一是认证网站的资料提交及审查流程、目前存在的问题以及改进措施，未来改进的目标是要使申请材料提交更规范、评审专家工作更方便；二是四家医院的审查和现场核查情况，包括各家医院的优势和存在的问题以及具体评分情况。汇总专家的评分后四家医院的得分情况为：烟台山医院94分、厦门市心脏中心88.14分、上海交通大学附属胸科医院93.25分、吉林大学第一医院98.3分。他指出目前各家胸痛中心存在的共性问题是：数据库的建立意识和原始记录资料的收集和保存意识不强，缺乏“没有记录就没有发生”的概念；缺乏流程改进工具和关系流程图；北京急救模式下的医院与120的深度合作存在困难。其中厦门心脏中心胸痛中心存在的主要问题是急诊科与心血管内科的合作存在困难，导致急性胸痛患者在急诊科延误时间过长以及各类诊疗流程没有得到落实。</p>
<p style="margin-bottom:100px; text-align:center "><img  style="width:500px; height:350px" src="../img/event_1_2.jpg"></p>
    <p style="text-indent:2em;line-height:30px; font-size:16px;"> 与会专家按照流程对四家医院审查结果进行了无记名投票，按照少数服从多数的原则，决定：烟台山医院、上海交通大学附属胸科医院、吉林大学第一医院通过首批认证，厦门市心脏中心给予3～6个月的时间进行部分改进，再进行评估。</p>
<p style="margin-bottom:100px; text-align:center "><img  style="width:500px; height:350px" src="../img/event_1_3.jpg"></p>
    <p><h4>二、关于认证标准和现场核查流程的修订</h4></p>
    <p style="text-indent:2em;line-height:30px; font-size:16px;">根据各位专家在前期认证工作发现的实际问题，会议经过充分讨论后对认证标准提出了以下修改意见：一是要在标准中增加对数据库的明确要求，其中主要是ACS的数据库必须完整，同时要求尽可能建立包括主动脉夹层及肺动脉栓塞在内的全部胸痛患者的数据库；二是部分条款的调整：在要素一的部分条目中，突出了绕行急诊和绕行CCU的要求。在要素三中，调整了负荷试验及重复采集心电图、肌钙蛋白的要求。另外还删除了部分重复的条款。
关于现场核查流程，经过充分讨论后会议做出如下修订意见：一是将微服私访作为常规现场核查内容；二是将网上资料审查内容和需要现场核查的内容分开评分，由专家在不同阶段分别打分，以防错误；三是将现场核查的内容进一步简化归类以提高现场核查效率。
</p>
<p style="margin-bottom:100px; text-align:center "><img  style="width:500px; height:350px" src="../img/event_1_4.jpg"></p>
<p><h4>三、中国胸痛中心认证标志</h4></p>
	<p style="text-indent:2em;line-height:30px; font-size:16px;">张岩教授介绍了认证标识的几种设计方案，经过讨论初步确定了认证标识的基本式样。</p>
    <p style="margin-bottom:100px; text-align:center "><img  style="width:500px; height:350px" src="../img/event_1_5.jpg"></p>
    <p><h4>四、今年的工作计划</h4></p>
    <p style="text-indent:2em;line-height:30px; font-size:16px; ">向定成秘书长就今年的工作计划提出了初步建议，经过专家的讨论后大致确立了以下工作作为今年的主任工作计划：一是完成胸痛中心认证标准的修改和发布；二是完成两批认证单位的审查，原则上上下半年各一批，每批的数量可以根据申请的情况确定；三是举办两次胸痛中心规范化建设培训班，上半年在4月份的南方会期间举行，下半年在9月份第四届中国胸痛中心高峰论坛期间举办，同时要尽可能争取在国内其它大型学术会议期间亦举办胸痛中心建设专场学术活动，在积极普及、推广胸痛中心概念的同时为拟开展胸痛中心的单位提供人员和技术培训；四是加强中国胸痛中心专用网站建设，其中最重要的是要尽快完成在线申请和资料提交系统的升级，以确保方便认证专家工作。</p>
<p style="text-indent:2em;line-height:30px; font-size:16px; ">
最后霍勇教授和方唯一教授分别做了会议总结。霍勇教授指出中国胸痛中心的认证工作要坚持不懈的继续开展下去，首批认证工作组织得
非常成功，从中我们可以很清晰地看到各家胸痛中心的优势与不足。根据首批四家申请单位的运作情况，我们可以梳理出目前在中国进行胸痛中心建设所能遇到的一些实际问题。比如数据库的建设就是目前普遍存在的共性问题。数据库的建立和规范运作有助于胸痛中心流程的持续改进，也是提供审查资料时最有说服力的证据。数据库的建立既不能好高骛远，也不能流于形式，要从各地的实际情况出发，逐步建立完善和全面的数据库，还应考虑逐渐与现有的数据库系统进行对接，方便管理和统计的需要。方唯一教授在总结中指出，在我国积极推进胸痛中心的建设是势在必行之事，而认证工作就是更好地促进各地胸痛中心建设的重要举措。此次认证工作时间短、效率高，各项工作均卓有成效，为下一步全面开展认证工作打下了良好的基础。认证结束后，认证办公室应与各申请单位加强沟通，指出优势与不足，促进各单位的进一步发展和改进。同时，还要大力推行胸痛中心的理念，与有意进行第二批认证的胸痛中心进行联系和培训，时机成熟时，积极启动第二次认证工作。
</p>
