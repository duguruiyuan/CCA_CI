        <p style="text-indent:2em;line-height:30px; font-size:16px;">中国胸痛中心认证工作委员会副主任委员兼秘书长向定成教授做了2014年工作报告，认证工作委员会2014年主要完成了以下工作任务：①逐步完善认证标准：在认证过程中根据专家反馈意见先后对认证标准进行了两次修订，使之更适合中国国情，贴近现实，更容易被大家所理解和接受；②完成了认证专家培训工作：到本次会议期间，完成两次共35名专家的培训，以统一对认证标准的理解并提高认证工作水平；③推动中国胸痛中心的快速发展：目前在中国胸痛中心网进行注册的医院有96家，从分布情况来看，北京、广东、东北发展速度比较快，华东地区还有较大提升空间，西部地区进步较快。④认证工作进展：全年共完成了三批认证工作，目前已经有19家医院通过了中国胸痛中心认证，目前正在进行第四批认证，已进入到提交材料阶段。⑤基本完成了中国胸痛中心认证专用数据库的设计和构建，并进行了初步测试和试用，拟在第四批认证中正式采用。⑥制作了胸痛中心公益宣传广告片及急诊PCI知情同意辅助讲解挂图，帮助各胸痛中心进行社区人群教育及易化知情同意过程，目前已经收到了很好的反馈。</p>
