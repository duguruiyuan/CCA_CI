       <p style="text-indent:2em;line-height:30px; font-size:16px; font-weight:500">中国胸痛中心认证工作委员会执行主任委员方唯一教授作了2015年工作计划的报告，初步确定2015年的主要工作任务如下：①本年度内完成3批认证工作任务；②加大胸痛中心宣传力度和推广胸痛中心建设经验；③进一步补充、修订、完善认证标准；④从第四批认证开始启用中国胸痛中心认证管理专用数据云平台，并尽可能使该数据库与即将启动的STEMI2项目数据库实现共享或兼容；⑤筹备建立中国胸痛中心质控中心，实现对已经通过认证胸痛中心的质量控制和促进持续改进；⑥筛选中国胸痛中心认证示范基地；⑦积极筹备尽早成立中国胸痛中心联盟。</p>
