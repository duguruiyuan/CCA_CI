      <p style="font-size:16px; font-weight:600">会议的主要内容：</p></div>
      <p style="text-indent:2em;line-height:30px; font-size:16px;">本次会议是在已经完成了三批胸痛中心认证工作之后，经霍勇主任提议召开的。根据2014年12月12日中国胸痛中心认证工作委员会第三次执委会的讨论意见，会议之前进行了增补认证工作委员会成员的准备工作，经委员提名、专家投票，共确定新增16名教授为中国胸痛中心认证工作委员会委员，他们是：刘惠亮、黄岚、关怀敏、蔡琳、李国庆、从洪良、李春洁、王景峰、刘峰、杨丽霞、万征、袁祖贻、傅向华、徐亚伟、李保、聂绍平。霍勇教授在会议致辞前正式宣布了新增委员名单。</p>
     <p style="margin-top:50px; text-align:center "> <img width="600px" src="/assets/img/mettingnews/news_4_1.jpg"></p>
<p style="text-indent:2em;line-height:30px; font-size:16px;">霍勇教授在致辞中指出中国胸痛中心认证工作已经开展了2年，完成了3批认证工作。从胸痛中心认证框架的制定到认证工作的开展，认证专家做了大量的工作，收到了很好的成效，对专业、对行业均起到了示范作用。他还对未来胸痛中心认证工作提出三点要求：一是要清楚认识到中国胸痛中心认证工作的意义，从专业角度整合资源、提高效率，推动学科进步。从行业发展角度，努力促进中国胸痛中心急救模式的改变，带动整个医疗行业的模式转变和发展同时还要配合医改政策，把医疗体系和社会需求联系到一起；二是在认证工作中，做好自身建设，不断细化、完善、修订认证标准，做好质量控制与持续改进工作，并强调公开公正，对专业、对社会负责；三是胸痛中心建设未来的发展任重而道远，胸痛中心认证工作不仅对具体的心血管疾病救治起到了非常重要的作用，同时还能够为政府宏观决策提供数据依据。在未来，还要配合卫计委分级医疗的政策，大力发展全国非PCI医院胸痛中心的建设，让认证工作更加到位、职能更加全面。</p>
<p style="margin-top:50px; text-align:center "> <img width="600px" src="/assets/img/mettingnews/news_4_2.jpg"></p>
<p style="text-indent:2em;line-height:30px; font-size:16px; font-weight:500">王斐副主任代表国家卫计委医政医管局出席此次会议并发言。他对胸痛中心认证工作委员会一年来开展的各项工作给予了充分的肯定，并对各位专家表示衷心感谢。他指出：国家卫计委医政医管局对中国胸痛中心认证工作高度关注，全力支持，目前国家在医改方向上正着力提升各级别医疗水平及服务能力，并提出了16字方针：基层首诊、上下转诊、急慢分治、双向转诊，目的是重新构建我国分级诊疗模式，这是一个长期的系统工程，涉及到行业管理及医疗从业人员等方方面面。胸痛中心的建设正好符合国家卫计委的发展方针，认证工作能促使我国急性胸痛相关疾病的救治水平迈上一个新台阶，推动整个急救医疗系统甚至整个医疗模式的改进和发展。</p>
<p style="margin-top:50px; text-align:center "> <img width="600px" src="/assets/img/mettingnews/news_4_3.jpg"></p>
<p style="text-indent:2em;line-height:30px; font-size:16px; font-weight:500">中国胸痛中心认证工作委员会副主任委员王伟民教授介绍了第三批认证情况，公布了第三批通过中国胸痛中心认证的9家医院名单并主持了授牌仪式。这9家医院是武汉亚洲心脏病医院、成都市第三人民医院、深圳市第四人民医院、上海交通大学医学院苏州九龙医院、徐州矿务集团总医院、新疆维吾尔自治区人民医院、天津市胸科医院、玉林市第一人民医院、广西医科大学第一附属医院。</p>
