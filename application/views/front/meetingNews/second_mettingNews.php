        <p style="text-indent:2em;font-size:16px;line-height:30px; "> 国胸痛中心认证工作委员会执行主任委员方唯一教授作了2016年工作计划的报告，初步确定2016年的主要工作任务如下：①2016年度内完成3批认证工作任务，初步计划在2016年7月22日执委会期间确定基层胸痛中心认证的实施方案，首批基层胸痛中心认证将与第九批认证工作同步展开。②继续强化胸痛中心规范化建设培训；拟举办全国胸痛中心规范化建设培训班3～4次，与企业、省市学术组织或医院合作举办区域性胸痛中心建设会议10-12次。③分中心办公室及示范基地建设；制订分中心办公室的工作机制，在2016年1月份前，各分中心办公室成立组织机构、完成人员招募，并进行培训，拟在2016年7月前开始实际运行，同时各示范基地每年还要举办一次区域性胸痛中心建设培训会议。④升级认证云平台；由认证办公室负责实施，在2016年1月前根据认证标准改建数据库并升级认证评审系统，争取在2016年5月前完善基层胸痛中心认证评审系统。</p>
        <p style="margin-top:50px; text-align:center "> <img width="600px"  src="/assets/img/mettingnews/news_2_4.jpg"></p>
    
      <p style="text-indent:2em; font-size:16px;line-height:30px; ">最后，与会专家对2015年的认证工作总结及2016年的工作计划进行了热烈的讨论。相对集中地讨论了以下几个主要问题：①部分专家提出微服私访的结果和评价指标应进一步细化，应尽可能量化并按一定权重比例纳入总分之中；微服私访的形式应尽可能多样化，以便更真实地评价申请单位胸痛中心的实际运行情况。②调整在线评估和现场核查的权重比，加大现场核查的比重。③应加强对已经通过认证的胸痛中心的质量监控，促进持续质量改进，防止出现认证后的质量滑坡。④多数专家对基层版胸痛中心的标准做了充分的肯定，同时也认为我国各地医疗资源及分配不均，基层胸痛中心的建设与认证存在相当的难度，建议对基层版胸痛中心标准在广泛征求意见的基础上进一步修订完善。</p>
       <p style="margin-top:50px; text-align:center "> <img width="600px"  src="/assets/img/mettingnews/news_2_5.jpg"></p>
     <p style="text-indent:2em; font-size:16px;line-height:30px; ">霍勇教授在会议总结时指出：目前我国胸痛中心建设事业已走向正轨，目前形势喜人，在进一步扩大认证规模的同时，要高度重视坚持认证标准和坚持公平、公正、公开的原则。全体委员要认真学习认证监督机制，仔细阅读回避制度、公示制度、申诉制度等，并在认证工作当中贯彻和落实，做到有理有据、合规合法，充分认识到认证工作的严肃性和公平性，杜绝认证工作中的不正之风，让认证工作在高效、廉洁、规范的轨道中继续发展壮大。</p>
