      <p>
      <h3 style="text-align:center; padding-top:30px;"> 中国胸痛中心认证工作委员会第三次执行委员会会</h3>
      <br>
      <h6 style="text-align:center; "> 中国胸痛中心认证工作办公室供稿 向定成 易绍东 夏斌</h6>     
      </p>
   
      <p style="text-indent:2em;line-height:30px; font-size:16px;">2014年12月12日，中国胸痛中心认证工作委员会第三次执委会在北京召开。霍勇、方唯一、向定成、陈纪言、王伟民、王乐丰、颜红兵、陈韵岱、于波、王焱、李建平、荆全民、陈玉国、秦伟毅、刘健、曲新凯、范觉新、郑杨等18位教授出席，中华医学会心血管病学分会宁田海、李冬梅、张岩主任以及认证办公室易绍东主任、夏斌秘书列席了此次会议。本次会议的主要内容是对第三批认证情况进行分析和总结、投票表决认证结果，讨论2014年工作总结以及2015年工作计划。</p>
     <p style="margin-top:50px; text-align:center "> <img width="600px" src="/assets/img/mettingnews/news_5_1.jpg"></p>

        
         <p style="text-indent:2em;line-height:30px; font-size:16px;">本次会议由工作委员会执行主任委员方唯一教授、副主任委员兼秘书长向定成教授主持。中国胸痛中心认证工作委员会主任委员霍勇教授在致词中指出，中国胸痛中心自主认证工作已经进入到实质性运作与规模化发展阶段，要加快胸痛中心认证进程、严格执行认证标准，不断完善各项流程，推动全国胸痛中心建设与发展，提高我国ACS救治效率，力争成为学科优化模式的典范。已经通过认证的单位要不断地持续改进，为全国拟建设或正在建设胸痛中心的单位提供观摩学习的机会，条件成熟时，还可建成一批培训基地，起到引领和示范作用。另外还要配合国家卫计委2015年心脑血管急救体系的建设，推动院前急救模式的转变，促进三甲医院的胸痛中心规范化建设，带动基层医院胸痛中心的发展，以期达到中国胸痛中心规模化建设的新阶段。
在霍勇教授致词之后，认证办公室易绍东主任详细汇报了第三批胸痛中心认证单位的核查情况，主要包括2014年下半年培训与认证过程以及改进措施；各家申请机构的在线审查、微服私访和现场核查情况的汇总；第三批申请认证的胸痛中心存在的一些共性问题。
全体参会专家针对9家胸痛中心的核查情况进行了认真的讨论并投票表决。
</p>
         <p style="margin-top:50px; text-align:center "> <img  width="600px" src="/assets/img/mettingnews/news_5_2.jpg"></p>	
        <p style="text-indent:2em;font-size:16px;line-height:30px; "> 本批共有30家医院提出申请，经中国胸痛中心认证办公室预审，有16家机构通过认证资格审查。经过网上提交认证材料、认证办公室对认证材料进行形式审查和内容复核等环节后，有9家机构进入到专家材料审核与现场核查阶段。全体委员对9家单位的审查结果进行了无记名投票，按照半数通过的原则，有6家胸痛中心通过第三批认证，分别是武汉亚洲心脏病医院、成都市第三人民医院、深圳市第四人民医院、上海交通大学医学院苏州九龙医院、徐州矿务集团总医院、新疆维吾尔自治区人民医院，还有3家单位需进行部分整改后，再行进一步评估。</p>
        <p style="margin-top:50px; text-align:center "> <img width="600px"  src="/assets/img/mettingnews/news_5_3.jpg"></p>
    
      <p style="text-indent:2em; font-size:16px;line-height:30px; ">最后，向定成教授对2014年的认证工作做了总结，并提出了2015年的工作计划。霍勇教授在会议总结时指出：整改单位一定要针对问题提出具体整改方案，尽快落实，提交相关证据。同时对胸痛中心建设提出了三点意见：一是要扩大公众影响力与社会影响力，通过媒体宣传进行有关胸痛疾病的全民健康教育。还要扩大专业影响力，发展已通过认证的单位负责人加入中国胸痛中心认证工作委员会专家团队，同时组织全国各医疗单位到已通过认证的胸痛中心去观摩，相互学习、共同促进；二是配合卫计委2015年大力发展500家县级医院的政策，推动县级医院胸痛中心的建设与基层网络医院的发展；三是认证工作也要持续改进，希望能建立统一数据库，通过对关键性指标的数据分析，进行质量监督和质量控制，促进胸痛中心建设和认证工作的可持续性发展。</p>
