      <p style="font-size:16px; font-weight:600">会议的主要内容：</p></div>
      <p style="text-indent:2em;line-height:30px; font-size:16px;"> 本次会议由中国胸痛中心认证工作委员会副主任委员兼秘书长向定成教授主持。他简要介绍了第七批认证的大致情况，并代表认证工作委员会对各位专家的辛勤工作表示了衷心的感谢。认证办公室易绍东主任详细汇报了对第七批申请认证单位的核查情况，主要内容包括在线审查、现场核查及微服私访情况的汇总以及最后总体得分情况，各家单位主要优点与不足的分析以及存在的一些共性问题。</p>
     <p style="margin-top:50px; text-align:center "> <img width="600px" src="/assets/img/mettingnews/news_1_2.jpg"></p>
       <p style="margin-top:50px; text-align:center "> <img width="600px" src="/assets/img/mettingnews/news_1_3.jpg"></p>
        
         <p style="text-indent:2em;line-height:30px; font-size:16px;"> 本批共有22家医院的胸痛中心提交认证申请，4家医院中途退出。经过网上提交认证材料、认证办公室对认证材料进行形式审查和内容复核等环节后，有15家单位进入到专家材料审核阶段，其中1 家单位未通过专家网上审查，最后共有14家单位进入到最后的现场核查，加上第六批还有3家医院需要重新评估，本次共有17家单位进入投票环节。全体与会专家针对17家胸痛中心的核查情况进行了认真的讨论并投票表决。按照半数通过的原则，有13家胸痛中心通过第七批认证，分别是：郑州市中心医院、玉溪市人民医院、第三军医大学第一附属医院、江苏大学附属医院、苏州市立医院、西安交通大学医学院第一附属医院、成都市第二人民医院、天津市第一中心医院、天津市北辰医院、四川大学华西医院、南昌大学第二附属医院、云南省楚雄州医院、周口市中心医院。还有3家单位需经整改后，再行进一步评估。另外1家单位未能通过本次认证。<br>
         霍勇教授在会议总结时指出：两年来的认证工作推动了全国各地胸痛中心建设的蓬勃发展，未来要进一步加快扩大规模和增强影响力。要树立一个长期的发展理念、建立有效的评价机制和标准化认证流程，确保胸痛中心在规范中高速发展，绝不能以降低准入门槛为代价来扩大规模，胸痛中心的认证必须坚持现有的衡量标准和评价模式，以规范严谨的认证工作促进胸痛中心建设的持续改进。拟定今年下半年开始正式启动基层胸痛中心的认证工作，这不仅是分级诊疗政策的体现，也是区域协同救治体系建设的关键环节。同时呼吁大家在各自承办的学术会议中加大胸痛中心的宣传力度，积极推动各地区胸痛中心相关理念的传播。此外，全体委员要要熟悉回避制度、公示制度、申诉制度等，并在认证工作当中贯彻和落实，让认证工作在高效、廉洁、规范的轨道中继续发展壮大。</p>
