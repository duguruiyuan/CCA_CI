        <p style="font-size:16px; font-weight:500 ">二、中国胸痛中心认证工作委员会主任委员霍勇教授部署了近期的几个重要工作安排：</p>
        <ol style="font-size:16px;" >
        <li>1.	加强认证工作的组织机构建设：鉴于认证工作量的日益增加，尤其是即将启动的非PCI医院认证将会大大增加认证办公室的工作量，在工作量激增的情况下不能放松认证的质量和标准。为此，建议成立分区域认证办公室，可从目前已经通过认证且胸痛中心建设较规范、具备认证组织工作能力并且愿意承担认证、示范和培训任务的单位进行筛选，由广州军区广州总医院进行统一培训、指导、协调组织认证工作。前期贯彻少而精的原则，与拟成立的胸痛中心示范基地一体化，由分会办公室统一协调运作经费，认证工作中要坚持公益性，避免过度商业化，此项工作由向定成主任负责。 </li>
        <li> 2.	制订非PCI医院认证标准，由向定成主任负责起草非PCI医院胸痛中心的认证标准，完成后提交认证工作委员会审定。</li>
        <li> 3.	落实胸痛中心的质量监控方案，由方唯一主任负责，解决已认证和待认证的中心如何在数据云平台上进行质量监控。</li>
        <li> </li>4.	设立中国胸痛中心认证监督委员会，负责对认证工作进行监督指导，同时负责处理投诉事件，由葛均波院士担任监事会主席，从已经通过认证的胸痛中心挑选不参与认证工作的专家组成，并成立办公室，负责接收投诉，基本工作流程为：办公室接收投诉邮件转发主席主席指定负责专家调查、取证提交委员会形成处理意见办公室反馈给投诉者。
        </ol>
