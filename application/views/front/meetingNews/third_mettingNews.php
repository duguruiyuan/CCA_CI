      <p style="text-indent:2em;line-height:30px; font-size:16px;">本次会议由中国胸痛中心认证工作委员会副主任委员兼秘书长向定成教授主持。中国胸痛中心认证工作委员会25名专家参会，中华医学会心血管病学分会、中国胸痛中心认证办公室等5人列席此次会议。主要内容是对第五批认证情况进行分析和总结、投票表决认证结果以及讨论有关胸痛中心认证的几个重要工作安排。</p>
