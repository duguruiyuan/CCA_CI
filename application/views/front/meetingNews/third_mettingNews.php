<p style="text-indent:2em;line-height:30px; font-size:16px; font-weight:500">一、第五批认证工作总结</p>
<p style="text-indent:2em;line-height:30px; font-size:16px; font-weight:500">会议首先由认证办公室易绍东主任汇报了第五批胸痛中心认证核查情况。主要包含以下内容：一是简单概括了第五批胸痛中心认证的全部过程与总体印象；二是5家未通过办公室预审及3家未通过专家网上预审的单位存在的共性问题；三是对19家（包括第四批二次复审的3家）进入现场核查单位的在线材料审查、微服私访和现场核查情况向全体认证工作委员会做了详细的汇报。部分参会专家对核查情况作了补充和说明并经行了认真的讨论。最后对19家胸痛中心进行了投票表决。</p>
<p style="text-indent:2em;line-height:30px; font-size:16px; font-weight:500">本批共有45家医院提出申请，经中国胸痛中心认证办公室预审，有32家单位获得参与认证资格。经过网上材料提交、认证办公室预审、专家在线审核评估后，有16家单位进入到现场核查和微服私访阶段，加上第四批二次复审的3家单位，一共是19家。全体委员对19家单位的审查结果进行了无记名投票，按照半数通过的原则，其中12家单位一次核查通过；3家单位整改后重新微服私访再行评估； 4家单位不通过，建议持续改进后重新申请认证。中国胸痛中心认证工作委员会执行主任委员方唯一教授宣布了投票结果（附表）。</p>

